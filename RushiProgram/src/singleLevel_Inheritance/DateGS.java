package singleLevel_Inheritance;

public class DateGS {
	
	private int date,month,year;
	
	public void setDate(int dd)
	{
		this.date=dd;
	}
	public void setMonth(int mm)
	{
		this.month=mm;
		
	}
	public void setYear(int yy)
	{
		this.year=yy;
	}
	
	public int getDate()
	{
		return date;
	}
	public int getMonth()
	{
		return month;
	}
	public int getYear()
	{
		return year;
	}

}
