package wraperWrittenExample;

public class LongValue {

	public static void main(String[] args) {
		
		Long l1=25l;
		Long l2=50l;
		
		
		Long i=l1.longValue();
		Long j=l2.longValue();
		
		System.out.println("Long Value is "+i);
		System.out.println("Long Value is "+j);

	}

}
