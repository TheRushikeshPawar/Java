package harachical_Inheritance;

public class Swift extends Maruti {
   
	private int engine;
	private String model;
	
	public void setEngine(int engine)
	{
		this.engine=engine;
	}
	public void setModel(String model)
	{
		this.model=model;
	}
	
	public int getEngine()
	{
		return engine;
	}
	
	public String getModel()
	{
		return model;

}
}
