package Day_2;

public class Increement_Decreement {

	public static void main(String[] args) {
		int count=1;
		System.out.println(count);
		count++;
		System.out.println("Count");
		int a=5,b=4,c=3;
		System.out.println(a++ + b * ++c);
		System.out.println("a = " + a + " b = " + b + " c = " + c);
		System.out.println(b*5/a + b++ + b);
		System.out.println("a = " + a + " b = " + b + " c = " + c);

	}

}
